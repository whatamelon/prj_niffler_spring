package poly.dto;

public class FeelDTO {
	private String feel_no;
	private String feel_val;
    private String feel_date;
	private String feel_weather;
	
	public String getFeel_no() {
		return feel_no;
	}
	public void setFeel_no(String feel_no) {
		this.feel_no = feel_no;
	}
	public String getFeel_val() {
		return feel_val;
	}
	public void setFeel_val(String feel_val) {
		this.feel_val = feel_val;
	}
	public String getFeel_date() {
		return feel_date;
	}
	public void setFeel_date(String feel_date) {
		this.feel_date = feel_date;
	}
	public String getFeel_weather() {
		return feel_weather;
	}
	public void setFeel_weather(String feel_weather) {
		this.feel_weather = feel_weather;
	}

}
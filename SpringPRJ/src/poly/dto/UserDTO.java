package poly.dto;

public class UserDTO {
	private String usr_no;
	private String usr_id;
	private String usr_name;
	private String usr_password;
	private String usr_job;
	private String usr_age;
	private String usr_hobby;
	
	public String getUsr_no() {
		return usr_no;
	}
	public void setUsr_no(String usr_no) {
		this.usr_no = usr_no;
	}
	public String getUsr_id() {
		return usr_id;
	}
	public void setUsr_id(String usr_id) {
		this.usr_id = usr_id;
	}
	public String getUsr_name() {
		return usr_name;
	}
	public void setUsr_name(String usr_name) {
		this.usr_name = usr_name;
	}
	public String getUsr_password() {
		return usr_password;
	}
	public void setUsr_password(String usr_password) {
		this.usr_password = usr_password;
	}
	public String getUsr_age() {
		return usr_age;
	}
	public void setUsr_age(String usr_age) {
		this.usr_age = usr_age;
	}
	public String getUsr_job() {
		return usr_job;
	}
	public void setUsr_job(String usr_job) {
		this.usr_job = usr_job;
	}
	public String getUsr_hobby() {
		return usr_hobby;
	}
	public void setUsr_hobby(String usr_hobby) {
		this.usr_hobby = usr_hobby;
	}

}